const array1 = [25, 14, 56, 15, 36, 56, 77, 18, 29, 49];
const max = Math.max(...array1);  //Math.max method finds the maximum number in a number list in the parenthesis.
const min = Math.min(...array1);  //Math.min method finds the minimum number in a number list in the parenthesis.

console.log("Original Array: ", array1) //print the array.
console.log("Maximum value for the above array = ", max); //print the maximum number in the array.
console.log("Minimum value for the above array = ", min); //print the minimum number in the array.
